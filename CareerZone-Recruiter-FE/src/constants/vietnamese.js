// Vietnamese typography and formatting utilities
export const VIETNAMESE_TYPOGRAPHY = {
  // Proper Vietnamese spacing rules
  formatCurrency: (amount) => {
    return new Intl.NumberFormat('vi-VN', {
      style: 'currency',
      currency: 'VND'
    }).format(amount);
  },

  // Format phone numbers Vietnamese style
  formatPhone: (phone) => {
    const cleaned = phone.replace(/\D/g, '');
    if (cleaned.length === 10) {
      return cleaned.replace(/(\d{4})(\d{3})(\d{3})/, '$1 $2 $3');
    }
    return phone;
  },

  // Format dates Vietnamese style
  formatDate: (date) => {
    return new Intl.DateTimeFormat('vi-VN', {
      day: '2-digit',
      month: '2-digit',
      year: 'numeric'
    }).format(new Date(date));
  },

  // Format time Vietnamese style
  formatTime: (date) => {
    return new Intl.DateTimeFormat('vi-VN', {
      hour: '2-digit',
      minute: '2-digit',
      hour12: false
    }).format(new Date(date));
  },

  // Vietnamese number formatting
  formatNumber: (number) => {
    return new Intl.NumberFormat('vi-VN').format(number);
  }
};

